int	ft_atoi(char *str)
{
	int	c;
	int	r;
	int	i;

	c = 0;
	r = 0;
	i = 1;
	while (str[c] <= 32)
		c++;
	while (str[c] == '+' || str[c] == '-')
	{
		if (str[c] == '-')
		{
			i *= -1;
		}
		c++;
	}
	while (str[c] >= '0' && str[c] <= '9')
	{
		r = (str[c] - '0') + (r * 10);
		c++;
	}
	return (r * i);
}
