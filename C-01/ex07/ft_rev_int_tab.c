void	ft_rev_int_tab(int *tab, int size)
{
	int	g;
	int	c;
	int	t;

	c = 0;
	while (c < size / 2)
	{
		g = tab[c];
		t = tab[size - c - 1];
		tab[size - c - 1] = g;
		tab[c] = t;
		c++;
	}
}
