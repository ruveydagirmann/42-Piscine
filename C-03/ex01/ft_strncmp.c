int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int				a;
	unsigned int	c;

	c = 0;
	a = 0;
	while (c < n && !a && s1[c] && s2[c])
	{
		a = s1[c] - s2[c];
		c++;
	}
	if (c < n && !a && (s1[c] || s2[c]))
		a = s1[c] - s2[c];
	return (a);
}
