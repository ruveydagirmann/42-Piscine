char	*ft_strcat(char *dest, char *src)
{
	int	a;
	int	c;

	c = 0;
	while (dest[c])
		c++;
	a = 0;
	while (src[a])
	{
		dest[c] = src[a];
		a++;
		c++;
	}
	dest[c] = '\0';
	return (dest);
}
